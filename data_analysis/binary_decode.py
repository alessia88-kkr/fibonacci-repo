# Decode ASCII binary string
binary_string = """01011001 01101111 01110101 00100111 01110110 01100101 00100000 01100011 01101111 01101101 
01110000 01101100 01100101 01110100 01100101 01100100 00100000 01110100 01101000 01100101 00100000 
01100001 01110011 01110011 01101001 01100111 01101110 01101101 01100101 01101110 01110100 00101110 
00100000 01000011 01101111 01101110 01100111 01110010 01100001 01110100 01110101 01101100 01100001 
01110100 01101001 01101111 01101110 01110011 00100001"""

# Remove whitespace and newlines
binary_clean = ''.join(binary_string.split())

# Process in 8-bit chunks
chars = [chr(int(binary_clean[i:i+8], 2)) for i in range(0, len(binary_clean), 8)]
decoded_text = "".join(chars)

print("Decoded message:")
print(decoded_text)
